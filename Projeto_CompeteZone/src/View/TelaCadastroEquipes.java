    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String idJogador = jTextField2.getText();
        String nomeEqp = jTextField1.getText();
        String num_players = jTextField4.getText();
        String jgsInscritos = jTextField3.getText();
        
        Equipes eqp = new Equipes();
        
        eqp.setIdJogador(Integer.parseInt(idJogador));
        eqp.setNome(nomeEqp);
        eqp.setNumJogadores(Integer.parseInt(num_players));
        eqp.setJgsInscritos(Integer.parseInt(jgsInscritos));
        
        try{
            DAO_Equipes deqp = new DAO_Equipes();
            deqp.Cadastrar(eqp);
            System.out.println("Cadastrado com sucesso!");
        }catch (ClassNotFoundException ex) {
            System.out.println("Erro: "+ex.getMessage());
            System.out.println("Erro ao cadastrar! .");
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex.getMessage());
            System.out.println("Erro ao cadastrar!");
        } catch (ParseException ex) {
            java.util.logging.Logger.getLogger(TelaCadastroJogadores.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

