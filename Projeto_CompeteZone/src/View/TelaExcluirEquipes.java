    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int i = JOptionPane.showConfirmDialog(null, "Deseja continuar?");
        
        if(i == JOptionPane.YES_OPTION) {
            Equipes eqp = new Equipes();
            DAO_Equipes dEqp = new DAO_Equipes();
            int id=Integer.parseInt(jTextField1.getText());
            eqp.setIdEqp(id);
        try {
            dEqp.Excluir(eqp);
        } catch (SQLException ex) {
            System.err.println("Erro"+ex);
        } catch (ClassNotFoundException ex) {
            System.err.println("Erro"+ex);
        }
        JOptionPane.showMessageDialog(null,"Excluído com Sucesso");
        }
        else if(i == JOptionPane.CANCEL_OPTION) {
            System.out.println("Clicou em Cancel");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Equipes eqp = new Equipes();
        int id = Integer.parseInt(jTextField1.getText());
        eqp.setIdEqp(id);
        try {
            DAO_Equipes dEqp = new DAO_Equipes();
            dEqp.pesquisar(eqp);
        } catch (SQLException ex) {
            System.out.println("Erro: "+ex.getMessage());
            //jStatus.setText("Erro ao buscar!");
        } catch (ClassNotFoundException ex) {
            System.out.println("Erro: "+ex.getMessage());
            //jStatus.setText("Erro ao cadastrar!");
        }
        jTextField2.setText(Integer.toString(eqp.getIdJogador()));
        jTextField3.setText(eqp.getNome());
        jTextField4.setText(Integer.toString(eqp.getNumJogadores()));
        jTextField5.setText(Integer.toString(eqp.getJgsInscritos()));
    }//GEN-LAST:event_jButton1ActionPerformed

